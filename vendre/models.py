class methodeDescription(models.Model):
    icone = models.CharField(max_length=140, choices=ICONE_CHOICES,
                             help_text="aller à http://getbootstrap.com/components/ pour les choix possible")
    ordre = models.PositiveSmallIntegerField()
    date = models.DateField()
    taille = models.CharField(max_length=2, choices=SIZE_CHOICES, default='md')
    nombre_colonnes = models.CharField(
        max_length=2, choices=COLUMN_CHOICES, default='3')
    nombre_offset = models.CharField(
        max_length=2, choices=COLUMN_CHOICES, default='0')
    contenu = models.TextField()
    confirm = models.BooleanField()

    class Meta:
        verbose_name = "Présentation des méthodes de ventes"
        verbose_name_plural = "Présentation des méthodes de ventes"

    def __str__(self):
        return self.icone


