    nom = models.CharField(max_length=140)
    titre = models.CharField(max_length=140)
    date = models.DateField()
    image_titre = models.ImageField(upload_to="vendre/image_titre/")
    contenu = models.TextField()
    couleur_text = models.CharField(max_length=140, default='black')
    couleur_titre = models.CharField(max_length=140, default='white')
    taille_text = models.PositiveSmallIntegerField()
    description = models.TextField()
    confirm = models.BooleanField()

    class Meta:
        verbose_name = "Présentation de l'onglet Vendre"
        verbose_name_plural = "Présentation de l'onglet Vendre"

    def __str__(self):
        return self.nom
